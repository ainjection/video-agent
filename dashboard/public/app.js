async function pollThumbnails() {
  const statusEl = document.getElementById('thumbStatus');
  try {
    const res = await fetch('/api/thumbnails/status');
    const s = await res.json();
    if (s.status === 'running' || (s.queue && s.queue.length)) {
      const progress = s.total ? `${s.done}/${s.total}` : `${s.done}`;
      statusEl.textContent = `Generating previews ${progress}${s.current ? ' · ' + s.current.id : ''}…`;
      // Refresh thumbnails in the grid when one completes
      document.querySelectorAll('.comp-thumb img').forEach(img => {
        const src = img.getAttribute('data-src');
        if (src && s.done > (img.getAttribute('data-rev') || 0)) {
          img.src = src + '?t=' + Date.now();
          img.setAttribute('data-rev', s.done);
        }
      });
      setTimeout(pollThumbnails, 2000);
    } else if (s.done + s.failed > 0) {
      statusEl.textContent = s.failed
        ? `Previews ready · ${s.done} succeeded, ${s.failed} failed`
        : `Previews ready · ${s.done} generated`;
      // Final refresh of thumbnails
      document.querySelectorAll('.comp-thumb img[data-src]').forEach(img => {
        img.src = img.getAttribute('data-src') + '?t=' + Date.now();
      });
      // Trigger comp list refresh to update hasThumbnail flags
      loadCompositions().then(() => renderLibrary(document.getElementById('librarySearch').value));
    } else {
      statusEl.textContent = '';
    }
  } catch (err) {
    // Server might not be up yet, retry quietly
    setTimeout(pollThumbnails, 3000);
  }
}

