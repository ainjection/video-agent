function effectiveDuration(clip) {
  if (clip.trimEnd != null && clip.trimStart != null) {
    return Math.max(1, clip.trimEnd - clip.trimStart);
  }
  return clip.durationInFrames;
}

function wireTrimHandle(handle, clip, edge, sourceFrames) {
  let dragging = false;
  let startX = 0;
  let startTrimStart = 0;
  let startTrimEnd = 0;

  handle.addEventListener('mousedown', (e) => {
    e.stopPropagation();
    e.preventDefault();
    dragging = true;
    startX = e.clientX;
    startTrimStart = clip.trimStart || 0;
    startTrimEnd = clip.trimEnd != null ? clip.trimEnd : sourceFrames;
    document.body.style.userSelect = 'none';
  });

  const onMove = (e) => {
    if (!dragging) return;
    const dx = e.clientX - startX;
    // 1 pixel = ~1 frame (rough; scales with clip visual width but this feels OK)
    const frameDelta = Math.round(dx * 1.5);
    if (edge === 'start') {
      const ns = Math.max(0, Math.min(startTrimEnd - 3, startTrimStart + frameDelta));
      clip.trimStart = ns;
    } else {
      const ne = Math.max(clip.trimStart + 3, Math.min(sourceFrames, startTrimEnd + frameDelta));
      clip.trimEnd = ne;
    }
    clip.durationInFrames = effectiveDuration(clip);
    renderTimelineUI();
  };

  const onUp = () => {
    if (dragging) {
      dragging = false;
      document.body.style.userSelect = '';
    }
  };

  document.addEventListener('mousemove', onMove);
  document.addEventListener('mouseup', onUp);
}

function renderTimelineUI() {
  const strip = document.getElementById('tlStrip');
  const hint = document.getElementById('tlDropHint');
  strip.classList.toggle('has-clips', timelineState.clips.length > 0);

  // Remove existing clips (keep the hint element)
  strip.querySelectorAll('.tl-clip').forEach(el => el.remove());

  timelineState.clips.forEach((clip, index) => {
    const comp = state.compositions.find(c => c.id === clip.compId);
    const fps = comp ? comp.fps : 30;
    const sourceFrames = clip.sourceDuration || clip.durationInFrames;
    const effFrames = effectiveDuration(clip);
    const trimmed = (clip.trimStart && clip.trimStart > 0) || (clip.trimEnd && clip.trimEnd < sourceFrames);
    const thumbUrl = comp && comp.hasThumbnail ? `/thumbnails/${encodeURIComponent(clip.compId)}.png` : '';
    const el = document.createElement('div');
    el.className = 'tl-clip';
    el.draggable = true;
    el.dataset.index = index;
    el.dataset.trimmed = trimmed ? 'true' : 'false';
    // Width scales with effective duration. 1.2 px/frame means a 1s clip is
    // ~36px and a 10s clip is ~360px — visible contrast when you trim.
    const pxPerFrame = 1.2;
    const width = Math.max(50, Math.round(effFrames * pxPerFrame));
    el.style.width = width + 'px';
    el.innerHTML = `
      <button class="tl-clip-remove" title="Remove">×</button>
      <div class="tl-trim-handle left" title="Trim start — drag left/right"></div>
      <div class="tl-trim-handle right" title="Trim end — drag left/right"></div>
      <div class="tl-clip-thumb">${thumbUrl ? `<img src="${thumbUrl}">` : ''}</div>
      <div class="tl-clip-title">${escapeHtml(clip.name)}</div>
      <div class="tl-clip-duration">${(effFrames / fps).toFixed(1)}s${trimmed ? ` <span style="color:var(--accent-2)">(trim)</span>` : ''}</div>
    `;
    strip.appendChild(el);

    el.querySelector('.tl-clip-remove').addEventListener('click', (e) => {
      e.stopPropagation();
      timelineState.clips.splice(index, 1);
      renderTimelineUI();
    });

    // Trim handles — drag to change trimStart / trimEnd
    wireTrimHandle(el.querySelector('.tl-trim-handle.left'), clip, 'start', sourceFrames);
    wireTrimHandle(el.querySelector('.tl-trim-handle.right'), clip, 'end', sourceFrames);

    // Reorder via drag-and-drop within the strip
    el.addEventListener('dragstart', (e) => {
      e.dataTransfer.setData('application/tl-clip-index', String(index));
      e.dataTransfer.effectAllowed = 'move';
      el.classList.add('dragging');
    });
    el.addEventListener('dragend', () => el.classList.remove('dragging'));
    el.addEventListener('dragover', (e) => {
      e.preventDefault();
      e.dataTransfer.dropEffect = 'move';
    });
    el.addEventListener('drop', (e) => {
      e.preventDefault();
      e.stopPropagation();
      const fromIdx = parseInt(e.dataTransfer.getData('application/tl-clip-index'), 10);
      const toIdx = parseInt(el.dataset.index, 10);
      if (isNaN(fromIdx) || fromIdx === toIdx) return;
      const [moved] = timelineState.clips.splice(fromIdx, 1);
      timelineState.clips.splice(toIdx, 0, moved);
      renderTimelineUI();
    });
  });

  // Update total (use effective duration)
  const totalFrames = timelineState.clips.reduce((s, c) => s + effectiveDuration(c), 0);
  const totalSec = (totalFrames / 30).toFixed(1);
  document.getElementById('tlTotal').textContent = `${timelineState.clips.length} clip${timelineState.clips.length === 1 ? '' : 's'} · ${totalSec}s (@30fps) · ${totalFrames}f`;

  // Debounced auto-generate-preview (only when clips >= 2). Creates a
  // "live" stitched comp behind the scenes and embeds Studio iframe.
  clearTimeout(renderTimelineUI._previewTimer);
  if (timelineState.clips.length >= 1) {
    renderTimelineUI._previewTimer = setTimeout(regenerateLivePreview, 1200);
  } else {
    const preview = document.getElementById('tlPreview');
    if (preview) preview.innerHTML = '<div class="preview-placeholder">Drop clips below → hit <b>Render Project</b> → preview appears here.</div>';
  }
}

// Generate a "live preview" timeline composition (no full render) and
// embed the Remotion Studio iframe pinned to it. Runs whenever the user
// edits the timeline after a debounce.
async function regenerateLivePreview() {
  if (!timelineState.clips.length) return;
  const preview = document.getElementById('tlPreview');
  if (!preview) return;

  // Check Studio status first — only bother if it's ready
  let studio;
  try {
    studio = await fetch('/api/studio/status').then(r => r.json());
  } catch { return; }
  if (studio.status !== 'ready') {
    preview.innerHTML = `<div class="tl-progress">Remotion Studio is <b>${escapeHtml(studio.status || 'unknown')}</b> — live preview loads when it's ready.</div>`;
    return;
  }

  try {
    // Live preview uses the persistent `TimelinePreview` composition +
    // noRender flag — rewrites the same file instead of spawning a new
    // TimelineProject-xxx entry on every keystroke. Studio iframe picks up
    // the change via its file watcher.
    const crossfade = parseInt(document.getElementById('tlCrossfade').value, 10) || 0;
    const res = await fetch('/api/timeline/render', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({
        preview: true,
        noRender: true,
        items: timelineState.clips.map(c => ({
          compId: c.compId,
          durationInFrames: effectiveDuration(c),
          sourceDuration: c.sourceDuration || c.durationInFrames,
          trimStart: c.trimStart || 0,
          trimEnd: c.trimEnd != null ? c.trimEnd : (c.sourceDuration || c.durationInFrames)
        })),
        musicUrl: timelineState.musicFile ? timelineState.musicFile.url : undefined,
        musicVolume: 0.5,
        crossfadeFrames: crossfade,
        fps: 30, width: 1920, height: 1080
      })
    });
    const data = await res.json();
    if (!res.ok || !data.project) {
      preview.innerHTML = `<div class="tl-progress" style="color:var(--danger)">${escapeHtml(data.error || 'preview generation failed')}</div>`;
      return;
    }
    timelineState.lastRenderedCompId = data.project.compId;
    const url = `${studio.url}/${encodeURIComponent(data.project.compId)}`;
    preview.innerHTML = `
      <iframe src="${url}" style="width:100%;height:100%;border:0;border-radius:6px;background:#000" allow="autoplay; clipboard-write"></iframe>
    `;
  } catch (err) {
    preview.innerHTML = `<div class="tl-progress" style="color:var(--danger)">${escapeHtml(err.message)}</div>`;
  }
}

async function renderTimeline() {
  if (!timelineState.clips.length) return;
  const preview = document.getElementById('tlPreview');
  preview.innerHTML = `
    <div class="tl-progress">
      <div>Stitching ${timelineState.clips.length} clips into a single composition…</div>
      <div class="bar"><div id="tlProgressFill" style="width:0%"></div></div>
      <div id="tlProgressLine" style="font-family:monospace;font-size:11px">generating project file…</div>
    </div>
  `;

  const crossfade = parseInt(document.getElementById('tlCrossfade').value, 10) || 0;
  let res, data;
  try {
    res = await fetch('/api/timeline/render', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({
        items: timelineState.clips.map(c => ({
          compId: c.compId,
          durationInFrames: effectiveDuration(c),
          sourceDuration: c.sourceDuration || c.durationInFrames,
          trimStart: c.trimStart || 0,
          trimEnd: c.trimEnd != null ? c.trimEnd : (c.sourceDuration || c.durationInFrames)
        })),
        musicUrl: timelineState.musicFile ? timelineState.musicFile.url : undefined,
        musicVolume: 0.5,
        crossfadeFrames: crossfade,
        fps: 30, width: 1920, height: 1080
      })
    });
    data = await res.json();
  } catch (err) {
    preview.innerHTML = `<div class="tl-progress" style="color:var(--danger)">✗ ${escapeHtml(err.message)}</div>`;
    return;
  }

  if (!res.ok) {
    preview.innerHTML = `<div class="tl-progress" style="color:var(--danger)">✗ ${escapeHtml(data.error || 'timeline render failed')}</div>`;
    return;
  }

  const renderId = data.render.id;
  const filename = data.render.filename;
  const es = new EventSource(`/api/renders/${renderId}/stream`);
  es.onmessage = (evt) => {
    const d = JSON.parse(evt.data);
    const fill = document.getElementById('tlProgressFill');
    const line = document.getElementById('tlProgressLine');
    if (fill) fill.style.width = d.progress + '%';
    if (line && d.line) line.textContent = d.line;

    if (d.status === 'done') {
      es.close();
      preview.innerHTML = `
        <video src="/out/${encodeURIComponent(filename)}" controls autoplay style="width:100%;height:100%;object-fit:contain;background:#000"></video>
      `;
      // Refresh composition list (new timeline comp was added)
      loadCompositions().then(renderTimelineLibrary);
    } else if (d.status === 'failed') {
      es.close();
      preview.innerHTML = `
        <div class="tl-progress" style="color:var(--danger);font-family:monospace;white-space:pre-wrap">${escapeHtml(d.line || 'render failed')}</div>
      `;
    }
  };
}

