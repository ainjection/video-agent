function renderLibrary(filter = '') {
  const grid = document.getElementById('libraryGrid');
  const q = (filter || '').toLowerCase().trim();
  const cat = state.currentCategory;
  const items = state.compositions
    .filter(c => !q || c.id.toLowerCase().includes(q) || (c.component || '').toLowerCase().includes(q))
    .filter(c => cat === 'All' || c.category === cat)
    .sort((a, b) => a.id.localeCompare(b.id));
  if (!items.length) {
    grid.innerHTML = '<div style="padding:60px 20px;color:var(--muted);font-style:italic;text-align:center;grid-column:1/-1">No compositions match.</div>';
    return;
  }
  grid.innerHTML = items.map(c => {
    const duration = (c.durationInFrames / c.fps).toFixed(1);
    const aspect = c.width === 1920 ? '16:9' : c.width === 1080 && c.height === 1920 ? '9:16' : `${c.width}x${c.height}`;
    const thumbUrl = c.hasThumbnail ? `/thumbnails/${encodeURIComponent(c.id)}.png` : '';
    const videoUrl = c.latestRender ? `/out/${encodeURIComponent(c.latestRender)}` : '';
    return `
      <div class="comp-card" data-id="${escapeAttr(c.id)}">
        <div class="comp-thumb">
          ${thumbUrl
            ? `<img data-src="${thumbUrl}" src="${thumbUrl}" alt="${escapeAttr(c.id)}" loading="lazy">`
            : '<div class="placeholder">🎞️</div>'}
          ${videoUrl ? `<video muted loop playsinline preload="none" data-src="${videoUrl}"></video>` : ''}
          <span class="category-badge">${escapeHtml(c.category || 'Other')}</span>
          <span class="aspect-badge">${aspect} · ${duration}s</span>
        </div>
        <div class="comp-card-body">
          <div class="comp-card-name">${escapeHtml(c.id)}</div>
          <div class="comp-card-meta">${c.width}×${c.height} · ${c.fps}fps · ${c.durationInFrames} frames</div>
        </div>
      </div>
    `;
  }).join('');

  // Lazy-load hover video: start playing only when user actually hovers
  grid.querySelectorAll('.comp-card video[data-src]').forEach(vid => {
    const card = vid.closest('.comp-card');
    let loaded = false;
    card.addEventListener('mouseenter', () => {
      if (!loaded) { vid.src = vid.getAttribute('data-src'); loaded = true; }
      vid.currentTime = 0;
      vid.play().catch(() => {});
    });
    card.addEventListener('mouseleave', () => { vid.pause(); });
  });
  grid.querySelectorAll('.comp-card').forEach(el => {
    el.addEventListener('click', () => selectComposition(el.getAttribute('data-id')));
  });
}

