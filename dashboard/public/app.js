function editBrandKit(id) {
  const kit = id ? state.brandkits.find(k => k.id === id) : {
    id: 'new-kit-' + Date.now(),
    name: 'New Brand Kit',
    colors: { primary: '#00e676', secondary: '#f59e0b', accent: '#ffffff', background: '#0a0a0a', text: '#ffffff' },
    fonts: { heading: 'Arial Black, sans-serif', body: 'Arial, sans-serif', mono: 'Courier New, monospace' }
  };
  const newName = prompt('Brand kit name:', kit.name);
  if (newName === null) return;
  kit.name = newName;
  const newId = prompt('Brand kit id (no spaces):', kit.id);
  if (newId === null) return;
  kit.id = newId.toLowerCase().replace(/\s+/g, '-');
  const primary = prompt('Primary colour (hex):', kit.colors.primary);
  if (primary) kit.colors.primary = primary;
  const secondary = prompt('Secondary colour (hex):', kit.colors.secondary);
  if (secondary) kit.colors.secondary = secondary;
  const bg = prompt('Background colour (hex):', kit.colors.background);
  if (bg) kit.colors.background = bg;
  saveBrandKit(kit);
}

