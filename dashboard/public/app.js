async function sendStudioMessage() {
  const input = document.getElementById('aistudioInput');
  const text = input.value.trim();
  if (!text) return;
  const sendBtn = document.getElementById('aistudioSend');
  sendBtn.disabled = true;
  input.value = '';

  studioState.messages.push({ role: 'user', content: text });
  renderStudioChat();

  // Show a pending placeholder for the assistant reply
  studioState.messages.push({ role: 'assistant', content: '…thinking', pending: true });
  renderStudioChat();

  try {
    const provider = document.getElementById('aistudioProvider').value;
    const res = await fetch('/api/ai/studio/chat', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({
        messages: studioState.messages.filter(m => !m.pending).map(m => ({ role: m.role, content: m.content })),
        provider
      })
    });
    const data = await res.json();
    if (!res.ok) throw new Error(data.error || 'chat failed');

    // Replace the pending placeholder with the real reply
    studioState.messages.pop();
    studioState.messages.push({
      role: 'assistant',
      content: data.reply,
      code: data.code || null,
      truncated: !!data.truncated,
      providerUsed: data.providerUsed
    });
    renderStudioChat();
  } catch (err) {
    studioState.messages.pop();
    studioState.messages.push({ role: 'assistant', content: '✗ ' + err.message, error: true });
    renderStudioChat();
  } finally {
    sendBtn.disabled = false;
    input.focus();
  }
}

function renderStudioChat() {
  const chatEl = document.getElementById('aistudioChat');
  const editBanner = studioState.editTargetCompId ? `
    <div class="aistudio-edit-banner" style="background:rgba(16,185,129,0.12);border:1px solid #10b981;border-radius:8px;padding:10px 14px;margin-bottom:14px;display:flex;justify-content:space-between;align-items:center;font-size:12px">
      <span>✎ Editing <b>${escapeHtml(studioState.editTargetCompId)}</b> — previews + saves overwrite the original file.</span>
      <button onclick="exitStudioEditMode()" style="background:transparent;border:1px solid #10b981;color:#10b981;border-radius:4px;padding:3px 8px;cursor:pointer;font-size:11px">Cancel edit</button>
    </div>
  ` : '';

  if (!studioState.messages.length) {
    chatEl.innerHTML = editBanner + '<div class="aistudio-welcome"><h3>Describe the video you want.</h3><p>Start by typing a brief below.</p></div>';
    return;
  }

  chatEl.innerHTML = editBanner + studioState.messages.map((m, i) => renderStudioMessage(m, i)).join('');
  chatEl.scrollTop = chatEl.scrollHeight;

  // Wire preview buttons
  chatEl.querySelectorAll('.aistudio-preview-btn').forEach(btn => {
    btn.addEventListener('click', () => studioPreviewCode(parseInt(btn.getAttribute('data-idx'), 10)));
  });
}

function renderStudioMessage(m, idx) {
  const avatar = m.role === 'user' ? 'Y' : 'AI';
  let body = m.content;
  // Render ```tsx blocks as <pre> for chat
  body = escapeHtml(body).replace(/```(?:tsx|typescript|ts)?\s*\n([\s\S]*?)```/g, (match, code) => {
    return `<pre>${escapeHtml(code)}</pre>`;
  });

  const truncatedWarning = m.truncated && !m.pending ? `
    <div class="code-actions" style="background:rgba(239,68,68,0.08);border:1px solid #ef4444;border-radius:6px;padding:10px 12px;font-size:11px;color:#ef4444;display:block">
      ⚠ Response cut off mid-output — the code above is incomplete. Tell me "please continue" or "rewrite it shorter" and I'll regenerate.
    </div>
  ` : '';

  const actions = m.code && !m.pending ? `
    <div class="code-actions">
      <button class="btn primary aistudio-preview-btn" data-idx="${idx}">▶ Render preview</button>
      <span style="color:var(--muted);font-size:11px;align-self:center">via ${escapeHtml(m.providerUsed || 'ai')}</span>
    </div>
  ` : (m.truncated ? truncatedWarning : '');

  const previewPanel = m.previewRenderId ? `
    <div class="aistudio-preview-panel" id="aistudio-preview-${idx}">
      ${m.previewStatus === 'done' ? `
        <video src="/out/${encodeURIComponent(m.previewFilename)}" controls autoplay></video>
        ${studioState.editTargetCompId ? `
          <div style="display:flex;gap:10px;margin-top:10px">
            <button class="btn primary" onclick="studioSaveEdit(${idx})">💾 Save to ${escapeHtml(studioState.editTargetCompId)}</button>
            <button class="btn danger" onclick="studioDiscardPreview(${idx})">✗ Discard</button>
          </div>
        ` : `
          <div style="display:grid;grid-template-columns:1fr auto auto;gap:10px;margin-top:10px">
            <input type="text" placeholder="Name to save to library" id="aistudio-name-${idx}" style="background:var(--panel);border:1px solid var(--border);color:var(--text);padding:9px 12px;border-radius:6px;font-size:13px">
            <button class="btn primary" onclick="studioSavePreview(${idx})">✓ Save</button>
            <button class="btn danger" onclick="studioDiscardPreview(${idx})">✗ Discard</button>
          </div>
        `}
      ` : `
        <div style="font-family:monospace;font-size:11px;color:var(--muted)">${escapeHtml(m.previewStatus || 'rendering…')}</div>
        <div style="margin-top:6px;height:4px;background:var(--border);border-radius:2px;overflow:hidden">
          <div style="height:100%;background:var(--accent);width:${m.previewProgress || 0}%;transition:width 0.2s"></div>
        </div>
      `}
    </div>
  ` : '';

  return `
    <div class="aistudio-msg ${m.role}${m.pending ? ' pending' : ''}">
      <div class="avatar">${avatar}</div>
      <div class="bubble">
        <div>${body}</div>
        ${actions}
        ${previewPanel}
      </div>
    </div>
  `;
}

async function studioPreviewCode(idx) {
  const msg = studioState.messages[idx];
  if (!msg || !msg.code) return;

  msg.previewStatus = 'starting…';
  msg.previewProgress = 0;
  renderStudioChat();

  try {
    const res = await fetch('/api/ai/studio/preview', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ code: msg.code, durationInFrames: 240, fps: 30, width: 1920, height: 1080 })
    });
    const data = await res.json();
    if (!res.ok) throw new Error(data.error || 'preview failed');

    msg.previewCompId = data.preview.compId;
    msg.previewFilename = data.render.filename;
    msg.previewRenderId = data.render.id;
    msg.previewStatus = 'rendering…';
    renderStudioChat();

    const es = new EventSource(`/api/renders/${data.render.id}/stream`);
    es.onmessage = (evt) => {
      const d = JSON.parse(evt.data);
      msg.previewProgress = d.progress;
      msg.previewStatus = d.line || d.status;
      if (d.status === 'done') {
        es.close();
        msg.previewStatus = 'done';
      } else if (d.status === 'failed') {
        es.close();
        msg.previewStatus = `failed — ${d.line || 'unknown'}`;
        // Give the AI the error so it can iterate
        studioState.messages.push({
          role: 'user',
          content: `The render failed with this error:\n\n${d.line}\n\nPlease fix the code.`
        });
      }
      renderStudioChat();
    };
  } catch (err) {
    msg.previewStatus = '✗ ' + err.message;
    renderStudioChat();
  }
}

window.studioSavePreview = async function (idx) {
  const msg = studioState.messages[idx];
  const nameInput = document.getElementById(`aistudio-name-${idx}`);
  const name = (nameInput && nameInput.value || '').trim();
  if (!name) { alert('Give it a name first.'); return; }
  try {
    const res = await fetch('/api/imports/finalise', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ previewCompId: msg.previewCompId, name })
    });
    const data = await res.json();
    if (!res.ok) throw new Error(data.error || 'finalise failed');
    flashBannerSuccess(`Saved as ${data.compId} — library refreshed`);
    msg.previewRenderId = null;
    msg.savedAs = data.compId;
    renderStudioChat();
    await loadCompositions();
    await loadCategories();
    renderCategoryTabs();
    renderLibrary();
    pollThumbnails();
  } catch (err) {
    alert('Save failed: ' + err.message);
  }
};

window.studioDiscardPreview = async function (idx) {
  const msg = studioState.messages[idx];
  try {
    await fetch('/api/imports/discard', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ previewCompId: msg.previewCompId })
    });
    msg.previewRenderId = null;
    msg.previewStatus = 'discarded';
    renderStudioChat();
  } catch (err) {
    alert('Discard failed: ' + err.message);
  }
};

function wireAIGenerate() {
  const drop = document.getElementById('aigenDrop');
  const input = document.getElementById('aigenFiles');
  drop.addEventListener('click', () => input.click());
  drop.addEventListener('dragover', (e) => { e.preventDefault(); drop.classList.add('hover'); });
  drop.addEventListener('dragleave', () => drop.classList.remove('hover'));
  drop.addEventListener('drop', (e) => {
    e.preventDefault();
    drop.classList.remove('hover');
    handleAigenFiles(e.dataTransfer.files);
  });
  input.addEventListener('change', (e) => handleAigenFiles(e.target.files));
  document.getElementById('aigenBtn').addEventListener('click', runAIGenerate);
}

const aigenState = { images: [] }; // [{filename, staticPath, url}]

async function handleAigenFiles(fileList) {
  const files = Array.from(fileList || []).slice(0, 4 - aigenState.images.length);
  if (!files.length) return;
  const form = new FormData();
  files.forEach(f => form.append('images', f));
  const statusEl = document.getElementById('aigenStatus');
  statusEl.classList.add('active');
  statusEl.textContent = `Uploading ${files.length} image${files.length === 1 ? '' : 's'}…`;
  try {
    const res = await fetch('/api/ai/upload-image', { method: 'POST', body: form });
    const data = await res.json();
    if (!res.ok) throw new Error(data.error || 'upload failed');
    aigenState.images.push(...data.files);
    renderAigenImages();
    statusEl.classList.remove('active');
  } catch (err) {
    statusEl.style.color = 'var(--danger)';
    statusEl.textContent = '✗ ' + err.message;
  }
}

function renderAigenImages() {
  const grid = document.getElementById('aigenImagesGrid');
  grid.innerHTML = aigenState.images.map((img, i) => `
    <div class="aigen-img-tile">
      <img src="${escapeAttr(img.url)}">
      <button class="aigen-img-remove" data-idx="${i}">×</button>
    </div>
  `).join('');
  grid.querySelectorAll('.aigen-img-remove').forEach(btn => {
    btn.addEventListener('click', () => {
      aigenState.images.splice(parseInt(btn.getAttribute('data-idx'), 10), 1);
      renderAigenImages();
    });
  });
}

async function runAIGenerate() {
  const description = document.getElementById('aigenDescription').value.trim();
  if (!description) return;
  const provider = document.getElementById('aigenProvider').value;
  const statusEl = document.getElementById('aigenStatus');
  const resultEl = document.getElementById('aigenResult');
  const btn = document.getElementById('aigenBtn');

  statusEl.classList.add('active');
  statusEl.style.color = 'var(--muted)';
  statusEl.textContent = '🎨 Generating block plan…';
  resultEl.innerHTML = '';
  btn.disabled = true;

  try {
    const res = await fetch('/api/ai/generate', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({
        description,
        provider,
        images: aigenState.images.map(i => i.staticPath)
      })
    });
    const plan = await res.json();
    if (!res.ok) throw new Error(plan.error || 'generate failed');

    statusEl.textContent = '🎬 Compiling plan → Remotion → rendering preview…';
    const renderRes = await fetch('/api/ai/render-plan', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ plan })
    });
    const renderData = await renderRes.json();
    if (!renderRes.ok) throw new Error(renderData.error || 'render-plan failed');

    streamAigenRender(plan, renderData);
  } catch (err) {
    statusEl.style.color = 'var(--danger)';
    statusEl.textContent = '✗ ' + err.message;
    btn.disabled = false;
  }
}

